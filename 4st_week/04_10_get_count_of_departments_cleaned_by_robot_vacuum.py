# r, c 의 좌표에서 시작, 배치된 칸을 청소한다 (+1)
# d의 동,서,남,북 중 하나에 따라서 r, c를 보고 0인지 1인지 탐색한다.
# 0이면 이동시키고 청소한다. 1이면 다시 r, c를 바꾸고 반복
# 네 방향 모두 1이면  한칸 후진한다. (방향에 따라 달라짐)
# 네 방향 모두 1이고 후진도 불가능하면 종료

# 동,서,남,북 의 방향에 따른 dc, dr 값을 정해놓자
# 동 = 0, 1 서 = 0, -1 남 = 1, 0 북 = -1, 0
def get_count_of_departments_cleaned_by_robot_vacuum(r, c, d, room_map):
    n = len(room_map)
    m = len(room_map[0])

    dr = [-1, 0, 1, 0]
    dc = [0, 1, 0, -1]
    room_map[r][c] = 2
    answer = 1 # 첫번째 배치된 칸에서 청소하니까

    queue = deque([[r, c, d]])

    while queue:
        r, c, d = queue.popleft()
        temp_d = d
        for i in range(4):
            temp_d = get_d_index_rotate_to_left(temp_d) # 북 -> 서
            new_r, new_c = r + dr[temp_d], c + dc[temp_d]

            if 0 <= new_r < n and 0 <= new_c < m and room_map[new_r][new_c] == 0:
                queue.append([new_r, new_c, temp_d])
                room_map[new_r][new_c] = 2
                answer += 1
                break

            if i == 3:
                new_r, new_c = r + dr[get_d_index_go_back(d)], c + dc[get_d_index_go_back(d)]
                queue.append([new_r, new_c, temp_d])

                if room_map[new_r][new_c] == 1:
                    return answer

    return answer

def get_d_index_rotate_to_left(d):
    return (d + 3) % 4

def get_d_index_go_back(d):
    return (d + 2) % 4

